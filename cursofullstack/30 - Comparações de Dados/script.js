let spaceship = "Golias"
let velocity = 80

// Igual a
console.log(velocity == 90) 

// Considera o tipo da variável (string/num) com ===
console.log(spaceship === "80")

// Diferente de 80
console.log(spaceship != 80)

// Diferente de 80 considerando o tipo da variável
console.log(spaceship !== "80")

// Considera se vem antes ou depois (alfabeto)
console.log(spaceship > "Helmet")

// Maior ou igual (funciona para texto)
console.log(velocity >= 90)

// Menor ou igual (funciona para texto)
console.log(velocity <= 120)